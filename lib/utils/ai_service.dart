  static Future<String> generateEmail({
    required String type,
    required String tone,
    required String context,
    String? recipientName,
  }) async {
    await Future.delayed(const Duration(seconds: 2));

    final typeLabels = {
      'reply': 'Reply',
      'cold': 'Cold Outreach',
      'follow_up': 'Follow-Up',
      'apology': 'Apology',
    };

    return '''Subject: ${_emailSubject(type, context)}

Dear ${recipientName ?? '[Recipient]'},

${_emailBody(type, tone, context)}

Best regards,
[Your Name]

---
Generated by ToolSpark AI
Type: ${typeLabels[type] ?? type} | Tone: $tone''';
  }

  static String _emailSubject(String type, String context) {
    switch (type) {
      case 'reply':
        return 'Re: $context';
      case 'cold':
        return 'Exciting Opportunity — $context';
      case 'follow_up':
        return 'Following Up — $context';
      case 'apology':
        return 'Our Sincere Apologies — $context';
      default:
        return context;
    }
  }

  static String _emailBody(String type, String tone, String context) {
    switch (type) {
      case 'reply':
        return '''Thank you for reaching out regarding $context. I appreciate you taking the time to share your thoughts on this matter.

After careful consideration, I'd like to address your points:

1. I completely understand your perspective and want to ensure we're aligned on the next steps.
2. Our team has reviewed the details and we're prepared to move forward with the proposed approach.
3. I'd suggest we schedule a brief call to finalize the remaining items.

Please let me know your availability this week, and I'll coordinate accordingly.''';
      case 'cold':
        return '''I hope this message finds you well. I'm reaching out because I believe there's a compelling opportunity related to $context that could significantly benefit your organization.

Here's what makes this worth your attention:

• We've helped similar companies achieve 40% improvement in their workflow efficiency
• Our solution integrates seamlessly with existing tools and processes
• The ROI typically becomes apparent within the first 30 days

I'd love to schedule a 15-minute introductory call to explore how we can create value for your team. Would any time this week work for a brief conversation?''';
      case 'follow_up':
        return '''I wanted to circle back on our previous conversation about $context. I understand you have a busy schedule, so I'll keep this brief.

Since we last spoke, there have been some exciting developments:

• We've launched new features that directly address the challenges you mentioned
• Several companies in your industry have recently come on board with great results
• I've put together a customized proposal based on our discussion

Would you have 10 minutes this week to reconnect? I'm confident we can find a solution that works perfectly for your needs.''';
      case 'apology':
        return '''I'm writing to sincerely apologize regarding $context. We take full responsibility for this situation and understand the inconvenience it has caused.

Here's what happened and what we're doing about it:

1. Root Cause: We've identified the underlying issue and have already implemented corrective measures.
2. Immediate Action: Our team has prioritized resolving this to ensure it doesn't happen again.
3. Going Forward: We've put additional safeguards in place to prevent similar occurrences.

Your trust means everything to us, and we're committed to making this right. Please don't hesitate to reach out if there's anything else we can do.''';
      default:
        return 'Thank you for your attention to $context.';
    }
  }

